package proyecto.com.pe.hiperbodega.logica;

import java.util.ArrayList;

public class Producto {
    private int idProducto;
    private int idCategoria;
    private String denominacion;
    private String presentacion;
    private double precioVenta;
    private String razonSocial;
    private int idBodega;
    private int cantidad;
    private double subtotal;

    private Boolean escogerProducto;

    public static ArrayList<Producto> ListaProducto= new ArrayList<>();

    public int getIdProducto() {
        return idProducto;
    }

    public void setIdProducto(int idProducto) {
        this.idProducto = idProducto;
    }

    public int getIdCategoria() {
        return idCategoria;
    }

    public void setIdCategoria(int idCategoria) {
        this.idCategoria = idCategoria;
    }

    public String getDenominacion() {
        return denominacion;
    }

    public void setDenominacion(String denominacion) {
        this.denominacion = denominacion;
    }

    public String getPresentacion() {
        return presentacion;
    }

    public void setPresentacion(String presentacion) {
        this.presentacion = presentacion;
    }

    public double getPrecioVenta() {
        return precioVenta;
    }

    public void setPrecioVenta(double precioVenta) {
        this.precioVenta = precioVenta;
    }

    public String getRazonSocial() {
        return razonSocial;
    }

    public void setRazonSocial(String razonSocial) {
        this.razonSocial = razonSocial;
    }

    public int getIdBodega() {
        return idBodega;
    }

    public void setIdBodega(int idBodega) {
        this.idBodega = idBodega;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    public double getSubtotal() {
        return subtotal;
    }

    public void setSubtotal(double subtotal) {
        this.subtotal = subtotal;
    }

    public Boolean getEscogerProducto() {
        return escogerProducto;
    }

    public void setEscogerProducto(Boolean escogerProducto) {
        this.escogerProducto = escogerProducto;
    }


}
